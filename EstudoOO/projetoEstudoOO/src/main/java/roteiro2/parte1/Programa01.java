package roteiro2.parte1;

public class Programa01 {
    public static void main(String[] args){
        int idade = 18;
        char letra = 'a';
        float numeroFloat = 3.14159f;
        double numeroDouble = 2.71828;
        String nome = "Isaac Newton";
        
        System.out.println("Exibir inteiro: "+ idade);
        System.out.println("Exibir letra: "+ letra);
        System.out.println("Exibir float: "+ numeroFloat);
        System.out.println("Exibir double: "+ numeroDouble);
        System.out.println("Exibir String: "+ nome);
                
    }
}
